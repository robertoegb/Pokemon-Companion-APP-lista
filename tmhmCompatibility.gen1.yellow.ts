/**
 * Checks if a Pokémon can learn a specific TM or HM.
 * @param pokemonId The National Pokédex ID of the Pokémon.
 * @param machineCode The code of the machine (e.g., 'tm26', 'hm03').
 */
export function canLearnTMHM(pokemonId: number, machineCode: string): boolean {
  const entry = POKEMON_TMHM_COMPATIBILITY[pokemonId];
  if (!entry) return false;

  const code = machineCode.toLowerCase();
  const num = parseInt(code.substring(2));

  if (isNaN(num)) return false;

  if (code.startsWith('tm')) {
    return entry.tms.includes(num);
  } else if (code.startsWith('hm')) {
    return entry.hms.includes(num);
  }

  return false;
}
